import Foundation

enum Margins {
    /// Standard layoutMargin of **8**.
    public static let standard: CGFloat = 8
    public static let standardHalf: CGFloat = 4
    public static let standard2x: CGFloat = standard * 2
    public static let standard3x: CGFloat = standard * 3
    public static let standard4x: CGFloat = standard * 4
    public static let standard5x: CGFloat = standard * 5
    public static let standard6x: CGFloat = standard * 6
    public static let standard7x: CGFloat = standard * 7

    /// Standard corner radius of **15pts**.
    public static let cornerRadius: CGFloat = 15
}
